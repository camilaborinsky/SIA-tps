def get_patterns(letters):
    patterns = []
    for letter in letters:
        patterns.append([])
        for vec in alphabet.get(letter):
            patterns[-1].extend(vec)
    return patterns


def get_noise_patterns(patterns, noise_prob):
    noise_patterns = []
    for pattern in patterns:
        noise_patterns.append([])
        for value in pattern:
            p = random.random()
            if(p >= noise_prob):
                noise_patterns[-1].append(value)
            else:
                noise_patterns[-1].append(math.copysign(1,value) * -1)
    return noise_patterns

def print_with_asterisks(letters):
    for letter in letters: 
        pattern = get_patterns([letter])[0]
        print(pattern)
        count = 1
        for item in pattern: 
            if item == 1:
                print(' * ', end='')
            else:
                print('   ', end='')
            if count % 5 == 0:
                print("") #Muevo a una nueva linea pero sin un enter de mas
            count += 1
        print("\n")

